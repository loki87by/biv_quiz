export const censoredChecker = (text) => {
  if (badWorksRegexps.every((reg) => !reg.test(text))) {
    const ind = politicalWords.findIndex((i) => i.regex.test(text));
    return ind < 0 ? text : politicalWords[ind].answer;
  } else {
    if (goodWords.includes(text.toLowerCase())) {
      return text;
    } else {
      return "";
    }
  }
};
